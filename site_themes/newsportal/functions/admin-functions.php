/**
 * Display top author set by limit
 * inspiration from http://dimox.net/wordpress-top-authors-list-without-a-plugin/
 *
 * @since 1.0
 * @param integer $number
 * @return array|false 
 */
function nesia_top_authors( $number = 10 ) {    
    
    $u = array();
    $authors = get_users();
    $users = array();
    
    if ($authors) {
        foreach ($authors as $author) {
            $u[] = $author->ID;
        }
        $authors = count_many_users_posts( $u );
        
        arsort( $authors );

        $i = 0;
        foreach ( $authors as $key => $value ) {
            $i++;
            if ($i > $number)
                break;
            
            $user = new WP_User( $key );
            $author_posts_url = get_author_posts_url($key);
            $post_count = $value;
            if ($post_count > 0) {
                $users[] = array( 'id' => $user->ID, 'name' => $user->display_name, 'post_count' => $post_count, 'url' => $author_posts_url );
            }
            
        }
    }
    
    // checking if no user found
    if( empty($users) )
        return false;
    
    return $users;
    
}

