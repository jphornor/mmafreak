/**
 * get related posts by
 *
 * @since 1.0
 *
 * @global object $post
 * @param string $related_by category|tags
 * @param integer $number_posts 5 posts for default
 * @param string $post_type 'post' is default
 * @return wp_query 
 */
function nesia_get_related_posts_by( $related_by = 'category', $number_posts = 5, $post_type = 'post' ) {
    global $post;
    
    $by = array();
    
    if($related_by == 'tags'){
        
        $tags = wp_get_post_tags($post->ID);
        foreach($tags as $individual_tag) $by[] = $individual_tag->term_id;
        
        
        $args=array(
                'tag__in' => $by,
                'post__not_in' => array($post->ID),
                'showposts'=> $number_posts, // Number of related posts that will be shown.
                'caller_get_posts'=> 1,
                'post_type' => $post_type
        );
        
    } else {
                
        $categories = get_the_category($post->ID);        
	foreach($categories as $individual_category) $by[] = $individual_category->term_id;

	$args=array(
		'category__in' => $by,
		'post__not_in' => array($post->ID),
		'showposts'=> $number_posts, // Number of related posts that will be shown.
		'caller_get_posts' => 1,
                'post_type' => $post_type
	);
        
    }
    
    return new wp_query($args);
        
}

