function nesia_short_url($url) {
	$service = get_option( 'nesia_url_shorten' );
	$bitlyapilogin = get_option( 'nesia_bitly_api_login' );;
	$bitlyapikey = get_option( 'nesia_bitly_api_key' );;
	if (isset($service)) {
		switch ($service)
		{
    		case 'TinyURL':
    			$shorturl = getTinyUrl($url);
    			break;
    		case 'Bit.ly':
    			if (isset($bitlyapilogin) && isset($bitlyapikey) && ($bitlyapilogin != '') && ($bitlyapikey != '')) {
    				$shorturl = make_bitly_url($url,$bitlyapilogin,$bitlyapikey,'json' );
    			}
    			else {
    				$shorturl = getTinyUrl($url);
    			}
    			break;
    		case 'Off':
    			$shorturl = $url;
    			break;
    		default:
    			$shorturl = $url;
    			break;
                }
	}
	else {
		$shorturl = $url;
	}
	return $shorturl;
}

