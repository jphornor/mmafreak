/**
 * Set excerpt lenght
 *
 * @since 1.0
 * @global object $post
 */
function nesia_the_excerpt( $lenght = 40, $more = true ) {
    global $post;
    
    $output = strip_shortcodes(get_the_excerpt());
    $output = str_replace(']]>', ']]&gt;', $output);
    $output = strip_tags($output);
    $words = explode(' ', $output, $lenght + 1);
    if(count($words) > $lenght) :
        array_pop($words);
        $output = implode(' ', $words);
    endif;
    
    $output = '<p>' . $output . '</p>';
    
    if( $more )
        $output .= '<p><a href="'. esc_url( get_permalink($post->ID) ) . '" class="more-link">' . __( 'Continue reading', 'nesia' ) . '</a></p>';
    
    echo $output;
    
}

